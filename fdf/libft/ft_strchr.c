#include <stdlib.h>

char	*ft_strchr(const char *s, int c)
{
	unsigned int	i;
	char			ch;

	ch = (char) c;
	i = 0;
	while (s[i])
	{
		if (s[i] == ch)
			return ((char *) &s[i]);
		i++;
	}
	if (s[i] == ch)
		return ((char *) &s[i]);
	return (NULL);
}
